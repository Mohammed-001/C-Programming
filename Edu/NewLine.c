#include <stdio.h>

int main (void)
{
  printf("This is C\n"); // '\n' breaks the line
  printf("This is new line\n\n"); //breaks two line
  printf("I am me\t I am me\t"); // '\t'Creates a horizontal tab
  printf("\""); // Inserts a double quote character
  printf("5\\10"); // '\\' Inserts a backslash character (\)
  return 0;
}